      SUBROUTINE head(X, Y)
      use OAD_active
      use w2f__types
      use oad_intrinsics
      IMPLICIT NONE
C
C     **** Parameters and Result ****
C
      type(active) :: X(1:2)
      type(active) :: Y(1:2)
C
C     **** Local Variables and Functions ****
C
      REAL(w2f__8) OpenAD_lin_0(1 : 2)
C
C     **** Top Level Pragmas ****
C
C$OPENAD INDEPENDENT(X)
C$OPENAD DEPENDENT(Y)
C
C     **** Statements ****
C
C$OPENAD XXX Template ad_template.f
      X(1)%v = (-1)
      OpenAD_lin_0 = SIGN(1.0D00,X%v)
      Y(1:2)%v = ABS(X%v)
      CALL zero_deriv(X(1))
      CALL sax(OpenAD_lin_0,X,Y(1:2))
      END SUBROUTINE
