C
      type(active) :: X(1:3)
      type(active) :: Y(1:3)
C
C     **** Top Level Pragmas ****
C
C$OPENAD INDEPENDENT(X)
C$OPENAD DEPENDENT(Y)
      interface
        subroutine foo(A,B,C,D)
        use w2f__types
      use OAD_active
        type(active) :: A
        type(active) :: B
        OPTIONAL  B
        type(active) :: C
        type(active) :: D
        OPTIONAL  D
        END SUBROUTINE
      end interface
           integer :: iaddr
          external iaddr
C
C     **** Statements ****
C
          if (our_rev_mode%plain) then
C original function
      CALL foo(A=X(1),C=Y(1))
      CALL foo(X(2),X(3),Y(2),Y(3))
          end if
          if (our_rev_mode%tape) then
C taping
      CALL foo(A=X(1),C=Y(1))
      CALL foo(X(2),X(3),Y(2),Y(3))
          end if
