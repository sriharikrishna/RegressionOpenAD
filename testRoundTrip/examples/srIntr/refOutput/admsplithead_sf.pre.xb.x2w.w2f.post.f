      IF (A%v.LT.B%v) THEN
        R%v = A%v
      ELSE
        R%v = B%v
      ENDIF
          end if
          if (our_rev_mode%tape) then
C taping
      IF (A%v.LT.B%v) THEN
        R%v = A%v
        OpenAD_Symbol_1 = 1_w2f__i8
        integer_tape(integer_tape_pointer) = OpenAD_Symbol_1
        integer_tape_pointer = integer_tape_pointer+1
