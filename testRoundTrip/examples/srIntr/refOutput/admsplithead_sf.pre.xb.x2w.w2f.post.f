C adjoint
      integer_tape_pointer = integer_tape_pointer-1
      OpenAD_Symbol_0 = integer_tape(integer_tape_pointer)
      IF (OpenAD_Symbol_0.ne.0) THEN
        A%d = A%d+R%d
        R%d = 0.0d0
      ELSE
        B%d = B%d+R%d
        R%d = 0.0d0
      ENDIF
          end if
